package com.jfinalshiromh.module.file.exception;


public class LLKFileException extends Exception {
    public LLKFileException() {
        super();
    }

    public LLKFileException(String message) {
        super(message);
    }

    public LLKFileException(String message, Throwable cause) {
        super(message, cause);
    }

    public LLKFileException(Throwable cause) {
        super(cause);
    }
}
